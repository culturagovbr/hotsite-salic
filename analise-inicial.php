                            <p>Inserir as etapas de trabalho do projeto, incluindo todas as fases do mesmo: pré-produção, produção e pós-produção. Este campo deverá contemplar todo o planejamento pretendido para realização do objeto informado, devendo estar em exata consonância com o Período de Realização, ou seja, a data de início corresponde ao início da pré-produção e a data final, ao final da pós-produção. Ressalta-se, também, que os períodos indicados em cada fase do projeto devem estar em concordância com os meses ou os dias correspondentes apontados na Planilha Orçamentária preenchida no escopo da proposta.</p>
                                                        
                            <div class="dept-title-tabs">Tenho um projeto que se encontra em fase de execução e preciso apresentar a sua próxima edição, é possível? Como faço?</div>
                            <p>Sim, é possível. O proponente deverá apresentar ao Ministério da Cultura uma declaração, a ser anexada nos documentos da Proposta, se comprometendo a somente executar o novo projeto proposto quando da apresentação da prestação de contas final do projeto cultural que se encontra em execução. Segue modelo: PROJETOS DE CONTINUIDADE: Anexar no campo INFORMAÇÕES ADICIONAIS documento assinado pelo dirigente da instituição, condicionando a movimentação dos recursos captados (caso a proposta em tela seja transformada em projeto e o mesmo seja aprovado) à apresentação da prestação de contas do PRONAC XX-XXXX, comprometendo-se assim a só executar a próxima edição após o encerramento deste.</p>
                                                        
                            <div class="dept-title-tabs">Meu evento será totalmente gratuito, como faço para preencher o Plano de Distribuição?</div>
                            <p>O proponente deverá colocar a estimativa de público total esperada no campo "População de Baixa-Renda", constante do Plano de Distribuição.</p>
                                                        
                            <div class="dept-title-tabs">O que deve constar no campo "Democratização de Acesso"?</div>
                            <p>
                                O proponente deverá detalhar como serão doados ou vendidos os ingressos e quaisquer outros produtos resultantes do projeto, com descrição detalhada do público alvo, dos preços, dos critérios, das estratégias edas etapas do processo de distribuição e dos resultados esperados com o acesso do público. Este campo deverá estar em exata consonância com o Plano de Distribuição). Devendo conter as mesmas informações relacionadas a: distribuição gratuita de ingressos – patrocinadores, divulgação e população de baixa renda; total para venda – normal e promocional; preço unitário – normal e promocional. Além de instituir a distribuição obrigatória gratuita (mínimo de 10% do total), é necessária a adoção de pelo menos uma das medidas de democratização de acesso dispostas no Art. 30 da Instrução Normativa n° 1, de 24 de junho de 2013.
Destaca-se que, mesmo o evento sendo totalmente gratuito e sem distribuição de ingressos, é necessário informar o público alvo pretendido no evento e, ao preencher o Plano de Distribuição, inserir a
estimativa de público na coluna População de Baixa Renda.
                            </p>
                            
                            <div class="dept-title-tabs">Como apresentar uma proposta ao Mecanismo "Incentivo a Projetos Culturais" (Incentivo Fiscal Federal)?</div>
                            <p>Com vistas ao encaminhamento de propostas culturais ao MinC, o proponente deve se cadastrar no escritório virtual Novo Salic, na página http://novosalic.cultura.gov.br/.  Ao iniciar o cadastramento da proposta no Novo Salic, é essencial entender o que é e para que o projeto está sendo elaborado, pois seu escopo deve estar fundamentado para análise. Alerta-se que, nessa fase, o conhecimento dos normativos que regem o mecanismo “Incentivo a Projetos Culturais” (Incentivo Fiscal Federal) que são: Lei no 8.313, de 23 de dezembro de 1991, Decreto n° 5.761, 27 de abril de 2006, Instrução Normativa n° 1, de 24 de junho de 2013, e Portaria nº 116, de 29 de novembro de 2011, é determinante para a construção e aprovação da proposta. Ressalta-se que somente proponentes com o perfil abaixo podem propor projetos a serem submetidos ao mecanismo “Incentivo a Projetos Culturais”, previsto na Lei 8.313/91: O proponente, pessoa física ou pessoa jurídica, que comprove atuação e conhecimento na área cultural por intermédio de portfólio. No caso de pessoa jurídica, de direito público ou privado, com ou sem fins lucrativos, o ato constitutivo vigente também deve dispor expressamente sua finalidade cultural.</p>
                        </div>
                        
                        <div class="tab-pane active" id="divSituacao">
                            
                            <div class="dept-title-tabs">O que significa a situação "C20 - Parecer Técnico emitido"?</div>
                            <p>Significa que o projeto foi avaliado pela Unidade Vinculada e se encontra com o Parecer Técnico emitido, o qual será avaliado pela SEFIC com vistas à inclusão do projeto na pauta da CNIC ou o seu retorno à Unidade Vinculada para retificação.</p>
                                                        
                            <div class="dept-title-tabs">Meu projeto se encontra com registro de aprovação após a Reunião da CNIC "D03 - Projeto Aprovado - Aguardando Análise Documental", quais os próximos passos?</div>
                            <p>
                                Após a avaliação do projeto pela CNIC, cabe à SEFIC decidir pela sua aprovação ou pelo seu indeferimento que ocorre por meio da assinatura do Termo de Decisão. No caso de aprovação, o processo seguirá para análise documental com vistas ao seu encaminhamento para publicação da portaria que autoriza a captação de recursos. 
                                <br>
                                Análise documental: 
                                <br>
                                Pessoas Físicas:
                                <br>
                                    <ul>
                                        <li>Cronograma de Execução;</li>
                                        <li>Validade da documentação do proponente;</li>
                                        <li>Certidão de Quitação de Tributos Federais (CQTF) e da Dívida Ativa da União (DAU);</li>
                                        <li>Cadastro Informativo de Créditos não Quitados do Setor Público Federal (CADIN).</li>
                                    </ul>
                                <br>
                                PessoaJurídica:
                                <br>
                                    <ul>
                                        <li>Cronograma de Execução;</li>
                                        <li>Fundo de Garantia por Tempo de Serviço (FGTS); Instituto Nacional de Seguridade Social (INSS);</li>
                                        <li>Certidão de Quitação de Tributos Federais (CQTF), da Dívida Ativa da União (DAU);</li>
                                        <li>Cadastro Informativo de Créditos não Quitados do Setor Público Federal (CADIN).</li>
                                    </ul>
                                <br>
                                Na impossibilidade de o Ministério da Cultura obter as certidões, será solicitado seu envio pelo proponente, por meio de diligência a ser registrada no Novo Salic.
                            </p>
                                                        
                            <div class="dept-title-tabs">Como preencher o Plano de Distribuição?</div>
                            <p>O Plano de Distribuição deve ser preenchido com os produtos a serem realizados no projeto cultural e que devem estar em consonância com a(s) ação(ões) definida(s)s no escopo da proposta. Tais produtos também devem constar do orçamento a ser preenchido no campo "Planilha Orçamentária". Essa(s) ação(ões) deve(em) dialogar com o(s) objetivo(s) discriminado(s) no escopo da proposta, que deve(em) ser passível(is) de mensuração na fase de Prestação de Contas do projeto cultural. O Plano de Distribuição deve estar em exata consonância com o campo Democratização de Acesso. Distribuição Gratuita Patrocinador: até 10%.Divulgação: até 10%.População de Baixa-Renda: mínimo de 10%.Comercialização:Valor Promocional: mínimo de 20%.Valor Normal: até 50%.Valores:Valor promocional: até R$50,00 Valor Normal: a critério do proponente desde que assegurada a democratização de acesso (valor a ser avaliado pelo MinC).</p>

