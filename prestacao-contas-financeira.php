                    <div class="tab-pane active" id="divComprovanteDeDespesas">
                        <div class="dept-title-tabs">Para comprovar pagamento de despesas com Direitos Autorais é necessária a nota fiscal?</div>
                        <p>A comprovação de Direitos Autorais é realizada por meio de recibo que descreva plenamente o direito concedido, contenha o nome e número do Pronac e outros dados essenciais como emissor, local, data, assinatura, e pelo boleto para pagamento.</p>
                        <div class="dept-title-tabs">Como comprovo despesa com artista estrangeiro?</div>
                        <p>Você deve emitir um recibo com os dados do artista (pessoa física), especificar o nº do Pronac e o nome do Projeto e anexar cópia do passaporte, caso o prestador do serviço tenha estado no Brasil. Se o recibo estiver em outro idioma deve ser traduzido, conforme artigo 11,  § 2º da IN nº 001 de 2013.</p>
                        <div class="dept-title-tabs">É obrigatório registrar os contratos de pessoas jurídicas quando da contratação dos prestadores de serviços dos projetos?</div>
                        <p>A formalização de contratos e os seus registros constituem segurança para as partes, mas não é necessário que contratos sejam enviados ao MinC quando da prestação de contas, salvo quando se tratarem de documentos indispensáveis para a comprovação das despesas, como por exemplo contratos de alugueis.</p>
                        <div class="dept-title-tabs">Como pago e comprovo uma veiculação de mídia feita por meio do Facebook?</div>
                        <p>Por meio de fatura. O pagamento deve ser realizado por  boleto bancário com o cheque da conta do projeto nominal ao fornecedor. Todos os documentos comprobatórios das despesas devem ter a descrição dos produtos e ou serviços o mais compatível possível com a rubrica orçamentária aprovada pelo MinC.</p>
                        <div class="dept-title-tabs">Entidade sem fins lucrativos emite nota fiscal?</div>
                        <p>O fato de um serviço ser prestado por uma associação ou entidade sem fins lucrativos e que tenha o título de Utilidade Pública não a exime de apresentar notas fiscais pelos serviços prestados. No entanto, para os casos em que a entidade não emita nota fiscal, deve ser enviada, no momento da prestação de contas, a disposição legal que confirme tal isenção.</p>
                        <div class="dept-title-tabs">O MEI emite nota fiscal?</div>
                        <p>O Microempreendedor Individual (MEI) é obrigado a emitir a nota fiscal nas vendas e nas prestações de serviços realizadas para outras pessoas jurídicas (empresas) de qualquer porte, ficando dispensado dessa emissão para o consumidor final, pessoa física, exceto se o consumidor exigir a sua emissão. Para mais informações, veirifque junto à Receita Federal.</p>
                        <div class="dept-title-tabs">Qual comprovante de despesa válido quando eu prestar serviços ao projeto no qual sou proponente?</div>
                        <p>É exigida a Nota Fiscal quando você (proponente) for prestador do serviço do projeto como Pessoa Jurídica.  Caso você seja o sócio da entidade proponente e prestar serviços como Pessoa Física, o comprovante de despesa é o Recibo de Pagamento a Autônomo (RPA) ou Recibo Simples. Por oportuno, observe sempre o conteúdo disposto no artigo 82 da IN MinC 1/2013</p>
                        <div class="dept-title-tabs">Sou proponente e prestei serviço ao projeto. O que fazer quando não for possível gerar nota fiscal com mesmo CNPJ para prestador e tomador de serviço?</div>
                        <p>Exclusivamente nos municípios onde não seja possível gerar Nota Fiscal com mesmo CNPJ para tomador e prestador do serviço, portanto, de impossibilidade de emissão da Nota Fiscal eletrônica, aceitamos fatura ou recibo, com o serviço e demais informações discriminadas, conforme orienta o §1º do artigo 83 da IN MinC 01/2013.</p>
                        <div class="dept-title-tabs">É possível considerar o reembolso de gasolina e pedágios se utilizando do item aprovado 'Passagens Terrestres', uma vez que será mais econômico para o projeto?</div>
                        <p>Não é possivel, pois os comprovantes de despesas devem ser sempre compatíveis com os itens aprovados. Acaso seja mais econômico o gasto com gasolina e pedágios, solicite readequação orçamentária diretamente no sistema Novo SALIC. </p>
                        <div class="dept-title-tabs">O que fazer para que as informações contidas em alguns cupons fiscais, comprovantes de despesas, não se apaguem ou se tornem ilegíveis com o passar do tempo?</div>
                        <p>Tire cópia ou digitalize os comprovantes de despesas assim que forem emitidos, pois muitos deles são impressos em papeis térmicos, cujas informações ali contidas podem se perder com certa facilidade. </p>
                        <div class="dept-title-tabs">Posso corrigir a Nota Fiscal Eletrônica ?</div>
                        <p>
                            A correção de nota fiscal pode ocorrer apenas para os casos em que o erro não esteja realcionado a variáveis que alterem:
                            <br>
                            1. O valor do imposto como: base decálculo, alíquota, diferença de preço, quantidade, valor da operação ou da prestação;
                            <br>
                            2. Dados cadastrais que impliquem em mudança de remetente ou destinatário;
                            <br>
                            3. Data de emissão ou de saída.
                        </p>
                        <div class="dept-title-tabs">Qual é o documento fiscal válido para compras de passagens aéreas realizadas por meio de agências de viagem, uma vez que estas não emitem nota fiscal ao consumidor final?</div>
                        <p>Para a prestação de contas são necessárias, como comprovante de despesa com passagens aéreas, as cópias dos cartões de embarque e a fatura da agência de viagem que contemple o detalhamento das informações: nome completo dos passageiros, trechos, datas, valor unitário e valor total. Todos os documentos devem conter o nome do projeto e número do Pronac.</p>
                        <div class="dept-title-tabs">Como comprovo despesas com passagens aéreas adquiridas pela internet?</div>
                        <p>É necessário enviar as cópias dos cartões de embarque e o comprovante da compra online que contemple o detalhamento das informações: nome completo dos passageiros, trechos, datas, valor unitário e valor total.</p>
                        <div class="dept-title-tabs">Como comprovar despesas com hospedagens no caso de compras realizadas por meio de agência de viagem?</div>
                        <p>Além da fatura emitida pela agência de viagem, solicite ao hotel documento de registro contemplando os detalhes da hospedagem, como o nome completo do hóspede, período, quantidade de diárias, valor unitário e valor total. Deve constar nesta declaração do hotel nome do projeto e número do Pronac.</p>
                        <div class="dept-title-tabs">Posso apresentar notas de hospedagem e de refeições, separadamente, no item orçamentário 'Hospedagem com Alimentação'?</div>
                        <p>Para o item “Hospedagem com Alimentação”, as refeições devem ser feitas, necessariamente, dentro do hotel. Não são aceitos, na prestação de contas, comprovantes fiscais de refeições fora do hotel quando a aprovação da despesa ocorreu como "Hospedagem com Alimentação".</p>
                        <div class="dept-title-tabs">No item "Hospedagem" posso locar apartamento oucasa por tempo determinado quando o custo for mais baixo?</div>
                        <p>Tendo em vista o princípio administrativo da economicidade, o item orçamentário “Hospedagem” pode ser realizado pela locação de apartamento ou casa por tempo determinado. Para a comprovação financeira, é necessário que você envie o Contrato de Locação e o Recibo (ou meio que identifique o recebimento pelo Locador, por exemplo, depósito identificado).  Todos os dcumentos devem estar identificados com o número de Pronac e nome do projeto.</p>
                        <div class="dept-title-tabs">Em especial, ao que devo me atentar sobre os comprovantes de despesas?</div>
                        <p>Toda despesa deve ser paga mediante apresentação de documentos ficais que devem:
                            <br>
                            - Especificar o serviço prestado ou material adquirido com igual descrição ao contido na planilha orçamentária aprovada pelo MinC.
                            <br>
                            - Constar a data de emissão a partir da data da publicação da portaria de aprovação e autorização para a captação de recursos (§1º do art. 35 do decreto 5761/2006 e caput do art. 49 da IN MinC 01/2013).
                            <br>
                            - Não ter data de emissão posterior ao término do prazo de execução do projeto.
                            <br>
                            - Ser emitido em nome do proponente.
                            <br>
                            - Trazer a indicação do número do Pronac e nome do projeto aprovado.
                            <br>
                            - Informar o desconto dos impostos de ISS, INSS e IR (quando houver).
                            <br>
                            Para caso de fornecedor Pessoa Jurídica, o documento fiscal aceito é a nota fiscal. Quaisquer outros documentos como recibo, boleto ou fatura serão aceitos apenas nos casos de não incidência de ISS ou ICMS e por disposição legal (que deve ser comprovada).
                            <br>
                            Para caso de fornecedor Pessoa Física, os documentos fiscais aceitos são recibo de pagamento de autônomo (RPA) ou Recibo de pagamento a contribuinte individual (RPCI) que devem conter: nome completo, CPF, endereço, local, data, assinatura e anuência do profissional ou fornecedor do serviço, detalhamento dos impostos cuja arrecadação é de sua responsabilidade.
                        </p>
                        <div class="dept-title-tabs">Em especial, ao que devo me atentar sobre os comprovantes de despesas?</div>
                        <p>Toda despesa deve ser paga mediante apresentação de documentos ficais que devem:
                            <br>
                            - Especificar o serviço prestado ou material adquirido com igual descrição ao contido na planilha orçamentária aprovada pelo MinC.
                            <br>
                            - Constar a data de emissão a partir da data da publicação da portaria de aprovação e autorização para a captação de recursos (§1º do art. 35 do decreto 5761/2006 e caput do art. 49 da IN MinC 01/2013).
                            <br>
                            - Não ter data de emissão posterior ao término do prazo de execução do projeto.
                            <br>
                            - Ser emitido em nome do proponente.
                            <br>
                            - Trazer a indicação do número do Pronac e nome do projeto aprovado.
                            <br>
                            - Informar o desconto dos impostos de ISS, INSS e IR (quando houver).
                            <br>
                            Para caso de fornecedor Pessoa Jurídica, o documento fiscal aceito é a nota fiscal. Quaisquer outros documentos como recibo, boleto ou fatura serão aceitos apenas nos casos de não incidência de ISS ou ICMS e por disposição legal (que deve ser comprovada).
                            <br>
                            Para caso de fornecedor Pessoa Física, os documentos fiscais aceitos são recibo de pagamento de autônomo (RPA) ou Recibo de pagamento a contribuinte individual (RPCI) que devem conter: nome completo, CPF, endereço, local, data, assinatura e anuência do profissional ou fornecedor do serviço, detalhamento dos impostos cuja arrecadação é de sua responsabilidade.
                        </p>
                        <div class="dept-title-tabs">Após o Prazo de Execução do projeto, tenho até trinta dias para prestar contas. Nesse prazo de 30 dias, podem ser emitidos comprovantes de despesas referentes a gastos do projeto e podem ser efetuados pagamentos?</div>
                        <p>Não. Despesas e pagamentos devem ser realizados até o fim do Prazo de Execução. O prazo limite para emissão de documentos fiscais comprobatórios é o último dia do Prazó de Execução.</p>
                    </div>

                    <div class="tab-pane" id="divPagamentos">
                        <div class="dept-title-tabs">Posso pagar despesas em dinheiro?</div>
                        <p>Você pode realizar o pagamento em dinheiro em espécie para apenas para despesas cujo valor não ultrapasse R$100,00 (cem reais), comprovadas por documento fiscal.</p>
                        <div class="dept-title-tabs">Como realizar os pagamentos do projeto?</div>
                        <p>Os pagamentos devem ser realizados por meio de cheque nominal, transferência eletrônica de fundos com identificação do destinatário ou por dinheiro em espécie, neste caso para despesas de até R$100,00.</p>
                        <div class="dept-title-tabs">Realizei uma despesa referente ao projeto antes da Portaria de Aprovação. Agora que o projeto foi aprovado, posso me ressarcir do valor gasto?</div>
                        <p>Não é possível  se ressarcir de despesas efetuadas em data anterior à publicação da portaria de autorização para captação de recursos.</p>
                        <div class="dept-title-tabs">Posso ser ressarcido de despesas?</div>
                        <p>
                            Após a portaria de autorização para captação, as despesas realizadas antes da liberação da movimentação dos recursos correm por conta e risco do proponente, somente sendo ressarcidas caso seja captado pelo menos 20% do valor aprovado (quando há a liberação da movimentação).
                            <br>
                            Para estes casos, o proponente deve reunir os comprovantes fiscais das despesas que pagou para o projeto, emitir um cheque nominal para si e fazer menção, no verso, de qual item orçamentário aprovado aquele cheque se refere, detalhando o nome e o número do Pronac, e declarando tratar-se de ressarcimento.
                        </p>
                        <div class="dept-title-tabs">Como deve se dar o pagamento ao exterior e a devida comprovação?</div>
                        <p>O pagamento  pode ser realizado por meio de contrato de câmbio, invoice ou outras formas de remessa ao exterior, desde que devidamente identificados emitente e recebedor. O mais usual dá-se por meio de contrato de câmbio junto ao Banco do Brasil, especificando o motivo da remessa. Quando da prestação de contas, além da cópia desse contrato, devem estar anexados os comprovantes fiscais correspondentes, hábeis, emitidos pelo país de origem, com a devida tradução, uma vez que se tratam de documentos expedidos no exterior, conforme cita o art. 11da IN nº 1/2013. É  preciso demonstrar a conversão da moeda estrangeira em Reais, para justificar o valor da despesa.</p>
                    </div>

                    <div class="tab-pane" id="divPrazos">
                        <div class="dept-title-tabs">Qual o prazo para eu enviar a Prestação de Contas?</div>
                        <p>O prazo para envio da Prestação de Contas é 30 dias após o término do Prazo de Execução. Importante reiterar que a prestação de contas traduz toda sua implementação. Durante a execução do projeto, você deve enviar relatórios trimestrais de acompanhamento. Assim, neste momento, você pode iniciar a sua prestação de contas financeira e qualquer dúvida ou dificuldade poderá ser dirimida tão logo ocorra.</p>
                        <div class="dept-title-tabs">Por qual meio eu apresento a prestação de contas?</div>
                        <p>A prestação de contas deve ser feita por meio do Novo Salic, endereço: http://novosalic.cultura.gov.br.</p>
                        <div class="dept-title-tabs">Qual o prazo limite para apresentação do recurso?</div>
                        <p>O prazo é 10 dias corridos, contados a partir da publicação da Portaria de Reprovação no DOU.</p>
                        <div class="dept-title-tabs">Por quanto tempo devo guardar as documentações?</div>
                        <p>Por dez anos, contados a partir da publicação da aprovação da prestação de contas.</p>
                    </div>

                    <div class="tab-pane" id="divRecursos">
                        <div class="dept-title-tabs">Sou proponente. Posso prestar serviços ao projeto?</div>
                        <p>Sim, você pode prestar serviços ao projeto desde que siga em conformidade com a Instrução Normativa (IN) MinC 01/2013.</p>
                        <div class="dept-title-tabs">Qual o limite da remuneração que o proponente pode receber no projeto?</div>
                        <p>Você iguala-se à condição de qualquer fornecedor. E, assim, deve observar a quantidade de serviços prestados ao projeto do §1º do art. 32 da IN MinC 01/2013. Atente-se que você deve fornecer o documento fiscal do serviço prestado para comprovar a despesa executada.</p>
                        <div class="dept-title-tabs">Gostaria de contratar um único fornecedor para a execução de mais de uma rubrica do projeto. É possível?</div>
                        <p>Sim, desde que siga a IN MinC 01/2013 que esclarece, entre outros pontos importantes, que é permitida a aquisição de mais de cinco produtos ou serviços do mesmo fornecedor apenas quando demonstre ser a opção de maior economicidade, comprovada com declaração do proponente acompanhada de cotação de preços de pelo menos dois outros fornecedores.</p>
                        <div class="dept-title-tabs">Posso contratar um única empresa para prestar serviços ao projeto?</div>
                        <p>Não é permitido uma única empresa realizar o projeto, ainda que seja feita licitação. A execução deve ser feita pelo proponente, que, deverá ter capacidade técnica para executar o projeto, conforme expresso no art. 40 da IN/2013.</p>
                        <div class="dept-title-tabs">Houve  atraso no cronograma do projeto e os recursos estão parados na conta movimento, sem aplicação financeira. Isso acarretará algum problema?</div>
                        <p>Sim. Os recursos devem ser ser aplicados em caderneta de poupança, quando a previsão de uso do valor for superior a um mês. Para casos em que o dinheiro seja utlizado em prazo inferior a 30 dias, deve-se aplicar em em fundo de aplicação financeira de curto prazo, ou operação de mercado aberto lastreada em título da dívida pública federal.</p>
                        <div class="dept-title-tabs">O dinheiro captado para o projeto foi aplicado e teve rendimentos. Como tive várias despesas extras durante a execução do projeto, posso utilizar este valor?</div>
                        <p>Você não pode utilizar o rendimento para pagamento de despesas extras, pois nenhuma despesa não aprovada pode ser realizada sem a anuência do MinC.</p>
                        <div class="dept-title-tabs">Posso fazer remanejamento de valores?</div>
                        <p>
                            É possível realizar ajustes que contribuirão para a efetividade de sua prestação de contas. Leia a Instrução Normativa (IN) MinC 01/2013 para compreender em detalhes. Alguns pontos de atenção:
                            <br>
                                - Alguns itens orçamentários podem sofrer variação em até 20% do seu valor aprovado. Caso seja verificado que será necessário modificar valores acima desse percentual, deve ser solicitada a readequação orçamentária no Novo Salic para aprovação do MinC.
                            <br>
                                - Mesmo dentro da alteração de 20% do item orçamentário, não é possível extrapolar o valor total aprovado das etapas ‘Custos Administrativos’, ‘Divulgação’ e ‘Captação’. Isto é, o orçamento dessas etapas não pode ser superior ao aprovado mesmo se ocorrerem modificações de valores de seus itens. Assim, caso um item orçamentário tenha acréscimo de 20% e seja verificado que será extrapolado o limite aprovado para a etapa, necessariamente outro item orçamentário da mesma etapa deverá ter diminuição de valor.
                            <br>
                                - O item orçamentário ou etapa “Remuneração para captação de recursos” não pode sofrer qualquer alteração de valores para mais
                        </p>
