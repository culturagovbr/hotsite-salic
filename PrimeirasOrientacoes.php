                    <h2>Conceitos Iniciais</h2>
                     <h4>O que é o Incentivo Fiscal a Projetos Culturais </h4>
                    <p>O incentivo é um mecanismo em que a União oferece, às pessoas físicas ou jurídicas, a opção de aplicar valores do imposto sobre a renda no apoio direto a projetos culturais a título de doações ou patrocínios.
                        <br><br> Ou seja: o Governo Federal oferece uma ferramenta para que a sociedade decida como aplicar parte do dinheiro de seus impostos em ações culturais. Desta maneira, o incentivo fiscal estimula a participação do cidadão e da iniciativa privada no aporte de recursos para o campo da cultura, ampliando o volume de recursos e atribuindo, assim, mais potência e mais estratégia econômica ao mercado cultural
                    </p>


                     <h2>Lei Rouanet </h2>
                     <p>A Lei 8.313, conhecida como Lei Rouanet, foi sancionada em 1991 e instituiu o Programa Nacional de Apoio à Cultura (Pronac), que estabelece as normativas de como o Governo Federal deve disponibilizar recursos para fomentar a cultura no Brasil. Para cumprir este objetivo, um dos mecanismos criados foi o "Incentivo a projetos culturais", também chamado de "Incentivo fiscal".</p>


                     <h2>Quem pode apresentar projetos  </h2>
                     <p>Todo projeto cultural, de qualquer artista, produtor e agente cultural brasileiro, pode se beneficiar desta Lei e se candidatar à captação de recursos de renúncia fiscal. Chama-se de ‘proponente’ a pessoa que apresenta proposta no âmbito do Pronac e responsabiliza-se pela execução do projeto. O proponente responde por todas as ações do projeto e deve prestar contas da correta utilização dos recursos públicos.</p>

                     <h3>A proponência pode ser feita por:  </h3>
                     <p>»	Pessoas físicas com atuação comprovada na área cultural<br>
                         »	Pessoas jurídicas de natureza cultural com, no mínimo, dois anos de atividade, podendo ser:<br>
                         &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;	Pessoas jurídicas públicas da administração indireta (autarquias, fundações culturais etc.)<br>
                         &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;	Pessoas jurídicas privadas com ou sem fins lucrativos (empresas, cooperativas, fundações, ONGs, organizações culturais etc.)
                     </p>


                     <h2>Apresentação de proposta </h2>
                     <p>O recebimento de propostas culturais no incentivo fiscal fica continuamente aberto entre 1º de fevereiro e 30 de novembro de cada ano.</p>
                     <p><img src="images/apresentacaoProposta.png" width="686px"></p>

                     <h3>1º passo: Estruturação do projeto cultural  </h3>
                     <p>O projeto cultural deve ter apresentação, objetivos e justificativa, bem como orçamento, etapas de execução, cronograma, plano de divulgação e plano de distribuição que garanta a democratização do acesso aos produtos gerados. Neste momento, realiza-se o cadastramento do usuário do Salic.</p>

                     <h3>2º passo: Envio dos documentos necessários.  </h3>
                     <p>O proponente é avaliado quanto aos requisitos necessários previstos na legislação. Consulte a lista de documentos no Artigo 11 da Instrução Normativa 01/2013.</p>

                     <h3>3º passo: Inscrição da proposta no Sistema de Apoio às Leis de Incentivo à Cultura (NovoSalic).   </h3>
                     <p>Após o envio da proposta, a tramitação ocorre integralmente através do Salic.</p>

                     <h2>Critérios de Avaliação de Projetos </h2>
                     <p>No incentivo fiscal, a análise é feita a partir da determinação da Lei Rouanet que diz, em seu Artigo 22, que os projetos enquadrados neste mecanismo "não poderão ser objeto de apreciação subjetiva quanto ao seu valor artístico ou cultural".</p>
                     <p>Se um projeto cumpre todas as normas e exigências da Lei, é considerado apto a captar recursos de incentivo fiscal. Consulte os pré-requisitos listados no Artigo 40 da Instrução Normativa 01/2013.</p>

                     <h2>Tramitação da Análise Inicial dos Projetos </h2>
                     <p>Durante a análise inicial, isto é, antes de o proponente receber a autorização para captação, os projetos são submetidos a um ciclo de apreciação que inclui três etapas (admissibilidade, análise e aprovação) com a apreciação de servidores públicos e profissionais representantes da sociedade civil. </p>
                     <p>Na admissibilidade, verifica-se se aquela proposta é de fato do campo cultural; se o proponente está qualificado conforme as regras; se o formulário no sistema foi devidamente preenchido em todos os campos necessários; e outras informações e documentos de acordo com a especificidade da proposta. </p>
                     <p>Na análise, caso admitida, a proposta segue para a unidade técnica correspondente ao segmento cultural do seu produto principal. Dentro do Sistema MinC, há unidades diferentes que lidam com universos artístico-culturais diferentes e que têm a competência de realizar esta tarefa. As secretarias e entidades vinculadas podem convocar pareceristas de seu próprio corpo de servidores ou do banco de peritos do MinC, que são profissionais credenciados por meio de edital público. O parecer técnico desta análise deve se manifestar quanto à adequação das fases, dos preços e orçamentos do projeto (que podem ter sugestões de ajustes), com recomendação de aprovação total, parcial ou indeferimento, devidamente fundamentada. </p>
                     <p>Em seguida, a proposta é encaminhada para a Comissão Nacional de Incentivo à Cultura (CNIC). A comissão reanalisa os projetos com base nos mesmos critérios objetivos, reflete sobre o custo-benefício dos produtos que propõem ser criados e endossa, ou não, os pareceres produzidos pelos peritos. Isto é feito em reuniões ordinárias mensais, que possibilitam decisões colegiadas e com transmissão online em tempo real. Após a CNIC, o projeto cultural segue para a fase de aprovação no MinC, onde é desenvolvido o termo de decisão com a aprovação ou indeferimento.</p>
                     <p><img src="images/Proponente.png" width="686px"></p>

                     <h2>Comissão Nacional de Incentivo à Cultura CNIC</h2>
                     <p>A Comissão Nacional de Incentivo à Cultura (CNIC) é um colegiado de assessoramento formado por representantes dos setores artísticos, culturais e empresariais, em paridade da sociedade civil e do poder público. Os membros da sociedade civil são oriundos das cinco regiões brasileiras, representando as áreas das artes cênicas, do audiovisual, da música, das artes visuais, do patrimônio cultural, de humanidades e do empresariado nacional. </p>
                     <p>A escolha destes integrantes é feita a partir de indicações de entidades representativas e habilitadas por meio de edital público, a cada dois anos. Desde 2011, as reuniões da CNIC acontecem também fora de Brasília, promovendo, em paralelo, atividades com a comunidade cultural das localidades visitadas, a exemplo do "Fórum de Fomento à Cultura – O Produtor Cultural e a Lei Federal de Incentivo à Cultura", englobando palestras, debates e oficinas de capacitação.</p>

                     <h2></h2>
                     <p>Os projetos culturais podem ser enquadrados no Artigo 18 ou no Artigo 26 da Lei Rouanet.</p>
                     <p>Quando o projeto é enquadrado no artigo 18, o apoiador pode deduzir 100% do valor investido, desde que respeitado o limite de 4% do imposto devido para pessoa jurídica e 6% para pessoa física.</p>
                     <p>O apoiador de um projeto enquadrado no artigo 26 pode deduzir, em seu imposto de renda, o percentual equivalente a 30% (no caso de patrocínio) ou 40% (no caso de doação), para pessoa jurídica; e 60% (no caso de patrocínio) ou 80% (no caso de doação), para pessoa física.</p>
                     <p>A Lei Rouanet define o enquadramento com base em segmentos culturais. São enquadrados no Artigo 18 os setores abaixo listados; tudo que não estiver previsto no Artigo 18 se enquadra no Artigo 26.</p>
                     <p>a) artes cênicas;<br>
                         b) livros de valor artístico, literário ou humanístico;<br>
                         c) música erudita ou instrumental;<br>
                         d) exposições de artes visuais;<br>
                         e) doações de acervos para bibliotecas públicas, museus, arquivos públicos e cinematecas, bem como treinamento de pessoal e aquisição de equipamentos para a manutenção desses acervos;<br>
                         f) produção de obras cinematográficas e videofonográficas de curta e média metragem e preservação e difusão do acervo audiovisual;<br>
                         g) preservação do patrimônio cultural material e imaterial;<br>
                         h) construção e manutenção de salas de cinema e teatro, que poderão funcionar também como centros culturais comunitários, em municípios com menos de cem mil habitantes.<br>
                     </p>




