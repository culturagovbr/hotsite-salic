                        <br><br>
                        2 -	Leia atentamente o decreto que regulamenta a lei e dá mais detalhes sobre a execução do Pronac: Decreto 5.791/2006.
                        <br><br>
                        3 -	Leia a portaria do MinC que regulamenta os segmentos culturais previstos na lei: Portaria 116/2011/MinC, com as atualizações da Portaria no. 5 de 26/01/2012 – DOU de 30/-1/2012.
                        <br><br>
                        4 -	Estude a Instrução Normativa (IN) 01/2013 do MinC para compreender o processo de gestão de um projeto desde a análise da proposta à prestação de contas: IN MinC 01/2013
                        <br><br>
