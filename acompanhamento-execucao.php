                    <div class="tab-content col-md-8 col-sm-8 col-xs-7 pull-right" style="text-align: justify;">

                        <div class="tab-pane active" id="divProjeto">
                            <div class="dept-title-tabs">É possível remanejar despesas da planilha financeira após a aprovação inicial pelo Ministério da Cultura?</div>
                            <p>Sim. Ressaltamos que não necessitam de prévia autorização do MinC alterações de itens dentro do limite de 20% para mais ou para menos do valor do item, desde que:
                                <br>
                                a) Não alterem o valor total do orçamento aprovado para o projeto;
                                <br>
                                b) Os remanejamentos não podem aumentar o valor aprovado para as etapas: Custos Administrativos, Divulgação e Captação de Recursos;
                                <br>
                                c) Os remanejamentos não podem ser feitos para itens retirados pelo MinC na aprovação.
                                <br>
                                Outras alterações que se pretenda fazer no orçamento aprovado deve ser submetida antecipadamente ao ministério para avaliação e só serão analisadas se houver captação de 20% do valor total aprovado (ou 1/12 dos recursos, no caso de Plano Anual de Atividades), a não ser que o projeto tenha sido contemplado por uma seleção pública ou tenha contrato de patrocínio que garanta a captação de 20% do valor aprovado.
                            </p>
                            
                            <div class="dept-title-tabs">Posso solicitar alteração do valor aprovado para a execução do projeto, com redução ou complementação de verba?</div>
                            <p>Sim. O pedido deve ser encaminhado, justificado, e com apresentação de nova planilha orçamentária, contendo o detalhamento das alterações efetuadas. A alteração de metas e redução de custos poderão ser autorizadas, desde que os objetivos do projeto não se alterem. Nestes casos, o projeto é reconduzido à unidade supervisionada para emissão de novo parecer técnico.
                                <ul>
                                    <li>A complementação orçamentária se limitará a 50% do valor aprovado e a análise estará condicionada à captação de, no mínimo, 50% do valor aprovado.</li>
                                    <li>A redução orçamentária se limitará a 40% do valor aprovado e estará condicionada à captação de, no mínimo, 20% do valor aprovado, ou caso tenha sido contemplado em seleções públicas ou respaldados por contrato de patrocínio que garantam a captação dos 20%. Para Plano Anual de Atividades, a captação deverá ser, no mínimo, 1/12 dos recursos.</li>
                                </ul>
                            </p>
                            
                            <div class="dept-title-tabs">Posso alterar a planilha orçamentária do projeto cultural, modificando a fonte de custeio de alguns itens?</div>
                            <p>Sim. Para este caso, deve ser encaminhada uma nova planilha orçamentária com a indicação dos itens que serão custeados com recursos diversos da Rouanet. É importante destacar que, junto à planilha orçamentária, o proponente deve encaminhar a comprovação de obtenção dos recursos de outras fontes, como contratos de patrocínio ou de prestação de serviços, ou, para casos de apoio de outros órgãos da administração pública, como prefeituras ou governos estaduais, o documento que comprove o respectivo apoio. O pedido deverá ser encaminhado via Salic, após captação de 20% dos recursos, ou via papel, anteriormente à captação mínima.  Para Plano Anual de Atividades,  a captação deverá ser, no mínimo, de 1/12 dos recursos</p>
                            
                            <div class="dept-title-tabs">Posso utilizar diferentes fontes de recurso para fazer o pagamento de um mesmo item orçamentário?</div>
                            <p>Não. É vedado o uso de diferentes fontes de financiamento para pagamento de um mesmo item de despesa.</p>
                            
                            <div class="dept-title-tabs">A inclusão de novo(s) item(s) na planilha orçamentária do projeto cultural, com remanejamento de despesas dentro do limite de 20% do valor de cada rubrica, necessita de prévia autorização do MinC?</div>
                            <p>Sim. Mesmo não alterando o valor total aprovado para o projeto cultural e não utlrapassando o limite de 20% estabelecido pela normativa do MinC, a inclusão de novos itens na planilha orçamentária do projeto deve ser submetida previamente ao MinC. O pedido só poderá ser encaminhado ao Ministério após a captação de 20% dos recursos aprovados (ou com comprovante de aprovação em seleção pública ou com respaldo de contrato de patrocínio, que garanta a captação de 20%) e os novos itens orçamentários não poderão ser iguais a itens retirados pelo MinC na aprovação inicial.  Para Plano Anual de Atividades,  a captação deverá ser, no mínimo, de 1/12 dos recursos.</p>
                            
                            <div class="dept-title-tabs">A redução (ou complementação) orçamentária do projeto cultural foi aprovada pelo MinC com um valor total abaixo do solicitado. Houve algum erro de cálculo?</div>
                            <p>Antes de concluir que houve erro de cálculo na análise do pedido, é preciso verificar se a redução ou aumento do valor de alguns itens orçamentários solicitados não foram autorizados, conforme legislação vigente ou entendimento do analista técnico da área, diante da realidade de mercado. Contudo, também se deve lembrar que, no caso de redução do valor global do projeto, o valor da rubrica referente à remuneração para captação de recursos e os custos da etapa Divulgação também serão proporcionalmente reduzidos.</p>

                            <div class="dept-title-tabs">Posso utilizar o saldo da aplicação financeira para pagamento de despesas do projeto cultural?</div>
                            <p>Sim. O saldo da aplicação financeira poderá ser utilizado tanto em projetos com captação parcial de recursos, quanto em projetos com captação total. Em ambas as situações:
                                <br>
                                a) Caso a readequação dos itens orçamentários fique limitada a 20%, para mais ou para menos, do valor da rubrica, não é necessário o envio de solicitação ao Minc; 
                                <br>
                                b) Caso a readequação apresente aumento ou redução dos valores das rubricas acima de 20% dos seus valores totais, o pedido deve ser encaminhado ao Ministério.
                            </p>
                            
                            <div class="dept-title-tabs">Posso alterar o plano de distribuição aprovado para o projeto?</div>
                            <p>Sim. A alteração no plano de distribuição (público alvo, quantidade do produto cultural, valores, dentre outras) deve ser submetida à análise do MinC, somente após a captação de 20% do valor total aprovado ou de 1/12 dos recursos, para Plano Anual de Atividades. No pedido, deve-se indicar claramente quais alterações se pretende fazer, atentando-se para as seguintes condições:
                                <br>
                                a) Caso o projeto seja inteiramente gratuito, deve-se manter a sua total gratuidade;
                                <br>
                                b) Caso o projeto apresente comercialização de produtos, deve-se manter preços populares e acessíveis e respeitar as porcentagens de distribuição, sobretudo a quantidade destinada à população de baixa renda.
                            </p>
                            
                            <div class="dept-title-tabs">Quando o produto cultural do projeto é realizado com diferentes fontes de recursos (incentivados ou não), os exemplares (unidades) pagos com recursos de outras fontes devem ser incluídos no plano de distribuição aprovado pelo MinC?</div>
                            <p>Sim. Para a distribuição do produto cultural, deve ser considerada a quantidade total de itens produzidos dentro do escopo do projeto, pois, uma vez que o produto foi realizado, mesmo que parcialmente, com recursos incentivados, todos os seus exemplares (unidades) devem ser considerados como resultado da proposta aprovada pelo MinC. No momento da apresentação da prestação de contas final do projeto, deve-se relatar a contribuição das outras fontes de recurso para a realização do produto.</p>

                            <div class="dept-title-tabs">As atividades e/ou os produtos culturais inicialmente propostos no projeto podem ser executados proporcionalmente ao percentual captado?</div>
                            <p>
                                Caso o proponente conclua que não conseguirá realizar a totalidade das atividades e/ou dos produtos culturais propostos, ele poderá solicitar a redução orçamentária do projeto ao MinC e, por conseguinte, a alteração proporcional das metas inicialmente aprovadas. Contudo, orientamos para que se atente às seguintes situações: 
                                <br>
                                    a) Caso <span style="text-decoration: underline">não haja</span> perpectivas de novas captações para o projeto e/ou o prazo de captação tenha finalizado: o pedido de redução orçamentária e alteração de metas poderá ser encaminhado ao MinC;
                                <br>
                                    b) Caso <span style="text-decoration: underline">ainda haja</span> a possibilidade de novos patrocínios para o projeto: aguarde o término do período de captação, para se certificar de que não haverá novos patrocínios e que o valor total do projeto poderá ser reduzido, e encaminhe o pedido ao MinC.
                                O pedido só poderá ser efetuado após a captação de, pelo menos, 20% dos recursos, ou caso tenha sido contemplado em seleção pública ou tenha contrato de patrocínio (que garanta a captação de 20%) e não deverá ultrapassar 40% do valor total aprovado. Para Plano Anual de Atividades,  a captação deverá ser, no mínimo, 1/12 dos recursos.
                            </p>
                        </div>
                        
                        <div class="tab-pane" id="divArquivamento">
                            <div class="dept-title-tabs">Quando devo solicitar o arquivamento do projeto?</div>
                            <p>Se  concluir que a execução do projeto é inviável, ou se não obtiver êxito na captação de recursos, devo formalizar o pedido de arquivamento junto à Coordenação Geral de Acompanhamento e Avaliação de Projetos.</p>
                            
                            <div class="dept-title-tabs">Como fazer a solicitação de arquivamento do projeto?</div>
                            <p>A solicitação de arquivamento será realizada por meio do envio de carta simples, assinada pelo proponente ou dirigente cadastrado no SALIC. Além disso, se houve captação de recursos, é necessário encaminhar anuência para o recolhimento dos recursos captados ao Fundo Nacional da Cultura.
IMPORTANTE: Ressaltamos que, para efetuar o recolhimento ao FNC, é necessário que tais recursos estejam disponíveis em conta, ou seja, eles não podem estar aplicados. Se for o caso, é necessário entrar em contato com o Banco do Brasil e solicitar a desaplicação dos recursos e desmarcar a opção de aplicação automática.</p>
                            
                            <div class="dept-title-tabs">Quando o projeto for arquivado ou quando a prestação de contas for apresentada, as contas do projeto serão encerradas?</div>
                            <p>Não. Quando o projeto for arquivado ou se prestação de contas foi apresentada, as contas serão bloqueadas pelo MinC para operações financeiras (crédito e débito) e somente serão encerradas pelo Banco do Brasil, após 2 anos sem movimentação, conforme Termo de Cooperação Técnica firmado entre o Banco do Brasil e o MinC.</p>
                            
                            <div class="dept-title-tabs">Caso meu projeto tenha sido arquivado, posso solicitar o desarquivamento?</div>
                            <p>Sim, desde que o pedido obedeça aos seguintes prazos:
                                <br>
                                a) A solicitação de desarquivamento de projeto ou reativação de proposta cancelada automaticamente, deverá ser justificada e feita em até dez dias corridos da data de registro do arquivamento no SALIC, desde que o proponente não tenha desistido formalmente.
                                <br>
                                b) Se o projeto tiver sido aprovado, mas arquivado antes da autorização para captação, a solicitação feita entre dez e sessenta dias poderá ser considerada, caso o projeto tenha sido contemplado em seleção pública de incentivador ou possua comprovada garantia de patrocínio.
                            </p>
                        </div>
                        
                        <div class="tab-pane" id="divImposto">
                            <div class="dept-title-tabs">Como é feita a dedução do Imposto de Renda?</div>
                            <p>A dedução é feita no momento do pagamento do imposto, ao fim do período fiscal do recolhimento. Ou seja, a empresa deixa de pagar à União a parcela da doação ou patrocínio, subtrai o valor do imposto apurado e recolhe para a União, por meio de Darf, apenas o imposto devido após a dedução do incentivo. A dedução poderá ser calculada do imposto devido determinado no lucro real. As pessoas jurídicas tributadas com base no lucro real só poderão deduzir o valor da doação e/ou do patrocínio como despesa operacional no caso de projeto enquadrado no Art. 26 da Lei 8313/91. No caso de pessoas físicas, a dedução será feita do imposto a pagar na declaração anual do Imposto de Renda.</p>
                            
                            <div class="dept-title-tabs">Pessoas Jurídicas tributadas com base no lucro presumido ou arbitrado podem deduzir o IR as doações e patrocínios a atividades culturais?</div>
                            <p>Não. Do imposto apurado com base no lucro presumido ou arbitrado não é permitida qualquer dedução a título de incentivo fiscal. Somente pessoas jurídicas tributadas pelo Lucro Real podem deduzir incentivos fiscais.</p>
                            
                            <div class="dept-title-tabs">Microempresas e EPP optantes pelo Simples podem deduzir as doações e patrocínios?</div>
                            <p>Não. As microempresa (ME) e empresas de pequeno porte (EPP), optantes pelo Simples, estão excluídas de qualquer benefício de dedução de incentivos do IRPJ.</p>
                            
                            <div class="dept-title-tabs">As doações ou patrocínios a atividades culturais sofrem retenção de IR na Fonte?</div>
                            <p>As transferências a título de doações ou patrocínios não estão sujeitas à incidência do imposto de renda na fonte.</p>
                        </div>
                        
                        <div class="tab-pane" id="divMarcas">
                            <div class="dept-title-tabs">Quando o produto cultural do projeto é realizado com diferentes fontes de recursos (incentivados ou não), os exemplares (unidades) pagos com recursos de outras fontes deverão apresentar as marcas do PRONAC?</div>
                            <p>Sim, todos os exemplares (unidades) do produto cultural devem apresentar as marcas do Governo Federal e da Lei Rouanet, pois, uma vez que o produto foi realizado, mesmo que parcialmente, com recursos incentivados, toda a sua tiragem (produção) deve ser considerada como resultado da proposta aprovada pelo MinC. No momento da apresentação da prestação de contas final do projeto, deve-se relatar a contribuição das outras fontes de recurso para a realização do produto.</p>
                            
                            <div class="dept-title-tabs">Quais são as regras de aplicação das marcas do Programa Nacional de Apoio à Cultura (PRONAC)?</div>
                            <p>As regras de aplicação e uso das marcas do Programa Nacional de Apoio à Cultura (PRONAC) estão registradas no Manual de Uso das Marcas do Pronac, disponível no endereço eletrônico <a href="http://www.cultura.gov.br">http://www.cultura.gov.br.</a></p>
                            
                            <div class="dept-title-tabs">Onde consigo os logotipos da Lei Federal de Incentivo à Cultura (Lei Rouanet), Ministério da Cultura e Governo Federal?</div>
                            <p>Os logotipos da Lei Federal de Incentivo à Cultura (Lei Rouanet), Ministério da Cultura e Governo Federal estão disponíveis para download no endereço eletrônico: <a href="http://www.cultura.gov.br/logotipos">http://www.cultura.gov.br/logotipos</a></p>
                            
                            <div class="dept-title-tabs">É necessário identificar os grupos no campo de logomarcas, como por exemplo “Apoio”, “Patrocínio”, etc?</div>
                            <p>O Manual de Uso das Marcas do Pronac não faz menção à identificação dos grupos no bloco de marcas (como, por exemplo, Patrocinadores, Apoio, etc..). Assim, tal identificação ficará a critério do proponente, desde que observadas as regras de aplicação das logomarcas estipuladas pelo citado manual.</p>
                            
                            <div class="dept-title-tabs">Meu material de divulgação foi indeferido com a justificativa de que a expressão “Ministério da Cultura apresenta” deve estar escrita por extenso. Como devo proceder nesse caso?</div>
                            <p>A expressão “Ministério da Cultura apresenta” (para projetos enquadrados no artigo 18 da Lei 8.313/1991) e suas variantes (inclusão do(s) nome(s) do(s) patrocinador(es)) deverá ser por extenso, não devendo conter logomarcas em seu corpo. As logomarcas devem ser aplicadas no bloco de marcas.</p>
                            
                            <div class="dept-title-tabs">Possuo um arquivo de áudio/vídeo para a análise, porém o sistema do Ministério da Cultura não permite a anexação de tais arquivos. O que faço?</div>
                            <p>Para análise do material de divulgação em áudio e/ou vídeo, os arquivos devem ser convertidos em documentos no formato .PDF e posteriormente anexados ao Novo Salic. Os arquivos de áudio devem ser transcritos e os em vídeo devem ter os frames que contêm as logomarcas capturados e convertidos em um arquivo.</p>
                            
                            <div class="dept-title-tabs">Quais são as extensões de arquivo permitidas para anexação no Novo Salic?</div>
                            <p>As extensões são BMP, GIF, JPEG, JPG, PNG, RAW, TIF e PDF, com limite de tamanho de 5MB cada.</p>
                            
                            <div class="dept-title-tabs">Como faço para anexar meu material de divulgação para análise?</div>
                            <p>A anexação deverá ocorrer no Novo Salic (<a href="http://novosalic.cultura.gov.br">http://novosalic.cultura.gov.br</a>), mediante link “Marcas”, localizado no campo lateral esquerdo da página do projeto.</p>
                            
                        </div>
                        
                        <div class="tab-pane" id="divPatrocinio">
                            <div class="dept-title-tabs">Qual o benefício de incentivar projetos culturais?</div>
                            <p>
                                Com o objetivo de incentivar as atividades culturais, por meio do incentivo é possível aplicar parcelas do imposto sobre a renda, a título de doações ou patrocínios no apoio direto a projetos culturais apresentados por pessoas jurídicas de natureza cultural e pessoas físicas, desde que os projetos atendam aos critérios estabelecidos no Programa Nacional de Apoio à Cultura - PRONAC. As empresas poderão deduzir do imposto de renda devido as quantias efetivamente despendidas em projetos aprovados pelo Ministério da Cultura, nos limites e condições estabelecidos na legislação do imposto de renda vigente:
                                <br>
                                a) doações; e,
                                <br>
                                b) patrocínios.
                            </p>
                            
                            <div class="dept-title-tabs">Qualquer empresa pode se beneficiar de incentivos fiscais, apoiando projetos culturais?</div>
                            <p>Não. Somente as Pessoas Jurídicas tributadas com base no Lucro Real podem deduzir do imposto de renda devido as doações ou patrocínios , observado o limite de 4% do imposto devido.  O Decreto 3.000 (Regulamenta a tributação, fiscalização, arrecadação e administração do Imposto sobre a Renda e Proventos de Qualquer Natureza.), de 26 de março de 1999, no artigo 246, estabelece as que são obrigadas a se enquadrar nesse regime de tributação.</p>
                            
                            <div class="dept-title-tabs">Meus parentes podem incentivar meu projeto?</div>
                            <p>Não podem incentivar: o cônjuge, os parentes até o terceiro grau, inclusive os afins, e os dependentes do doador ou patrocinador ou dos titulares, administradores, acionistas ou sócios de pessoa jurídica vinculada ao doador ou patrocinador, na data da operação, ou nos doze meses anteriores.</p>
                            
                            <div class="dept-title-tabs">Quanto o incentivador de projetos culturais pode deduzir de seu imposto de renda?</div>
                            <p>
                                Atualmente, existem 2 situações descritas na Lei 8313/91: 
                                <br>
                                1) O artigo 26 da Lei indica os percentuais de dedução do IR, limitados aos estabelecidos pela legislação do imposto de renda vigente, que atualmente são 4% do imposto devido para empresas e 6% do imposto devido para pessoa física. A empresa incentivadora poderá ainda lançar o valor incentivado como despesa operacional.
                                Os percentuais de dedução são os seguintes:
                                <br>
                                Empresas:
                                <br>
                                - até 30% do valor patrocinado;
                                <br>
                                - até 40% do valor doado
                                <br>
                                Pessoa física:
                                <br>
                                - até 60% do valor patrocinado;
                                <br>
                                - até 80% do valor doado.
                                <br>
                                2) Com a modificação implementada pela Lei 9.874/99, quem investe em projetos culturais enquadrados nos segmentos indicados pelo artigo 18 da Lei passou a ter a possibilidade de deduzir até 100% do valor doado ou patrocinado, também dentro dos limites da legislação do imposto de renda vigente (atualmente 4% para empresas e 6% para pessoa física). As empresas que investem nesses segmentos não podem lançar o valor incentivado como despesa operacional.
                            </p>
                            
                            <div class="dept-title-tabs">Quais os procedimentos necessários para a captação de recursos?</div>
                            <p>Antes do início da captação, o proponente deve aguardar  a abertura pelo MinC das contas correntes vinculadas ao projeto, pois toda movimentação de recursos captados deverá ser efetuada nelas. Após essa etapa, o proponente está habilitado a captar recursos junto aos incentivadores.</p>
                            
                            <div class="dept-title-tabs">Há diferença entre contrato de patrocínio e carta de intenção de patrocínio?</div>
                            <p>Sim. Carta de intenção de patrocínio não se iguala ao contrato de patrocínio, uma vez que apenas nesse último há a garantia de que as condições firmadas entre patrocinador e patrocinado serão cumpridas, bem como de que o(s) aporte(s) será(ão) realizado(s). Nesse documento constam o valor a ser aportado, a(s) data(s) do(s) aporte(s), as condições estabelecidas entre os interessados e as assinaturas dos resposnáveis reconhecidas em cartório. A carta de intenção, diferentemente, apenas informa sobre a possibilidade da ocorrência de patrocínio, não havendo comprometimento do possível patrocinador com o patrocinado.</p>

                        </div>
                        
                        <div class="tab-pane" id="divPrazo">
                            <div class="dept-title-tabs">Qual o prazo limite para pedir a prorrogação de prazo para captação de recursos e/ou de execução do projeto?</div>
                            <p>A solicitação de prorrogação de prazo para captação deverá ser feita com antecedência de, no mínimo, 30 dias do encerramento do prazo vigente, publicado no Diário Oficial da União. A solicitação de prorrogação de prazo de execução segue a mesma regra. </p>
                            
                            <div class="dept-title-tabs">Em quanto tempo o MinC analisa os pedidos de prorrogação?</div>
                            <p>Até 30 dias corridos para análise. </p>
                            
                            <div class="dept-title-tabs">O prazo de execução renova o de captação?</div>
                            <p>Não. O prazo de captação coincide com o de execução, ou seja, se eu estou apto a captar recursos, eu posso executar o projeto. Quando o pedido de prorrogação de prazo de execução, somente, é deferido, é indicado que não há interesse em captar mais recursos e há interesse somente em executar o projeto.</p>
                            
                            <div class="dept-title-tabs">Qual a diferença entre prazo de captação e execução?</div>
                            <p>A prorrogação do prazo de captação deve ser solicitada quando o proponente tem o interesse em continuar captando recursos. Para este caso, à medida que capta, pode, obviamente, executar as atividades previstas para o projeto. Entretanto, a extensão do prazo de execução não prorroga o prazo de captação. O proponente apenas deve solicitar a prorrogação do prazo de execução quando não mais tiver interesse em captar recursos para o projeto. Este caso se aplica exclusivamente para projeto que possui captação total ou suficiente para a realização das atividades. Nesse contexto, é erro encaminhar dois pedidos ao mesmo tempo, de captação e execução: ou o proponente capta (e pode executar) ou apenas executa os recursos já captados.</p>
                            
                            <div class="dept-title-tabs">O período de execução informado quando da apresentação da proposta garante que eu possuo prazo para executar o projeto?</div>
                            <p>Não. Esse período de execução é uma previsão de execução do projeto e irá determinar o acesso aos campos de prestação de contas no sistema. O prazo legal válido é o publicado em Diário Oficial da União ou informado em Ofício quando é solicitada a prorrogação do período de execução.</p>
                            
                            <div class="dept-title-tabs">Se o projeto cultural foi aprovado para execução no período de dois anos, devo solicitar prorrogação de prazo de captação/execução no final do primeiro ano?</div>
                            <p>Sim. Quando o projeto é aprovado, o prazo de captação é publicado no Diário Oficial da União e, este prazo, está limitado ao exercício fiscal em que o projeto foi aprovado. Por isso, caso haja a intenção de prorrogar a captação ou execução, deve-se solicitar a prorrogação pelo SALIC com antecedência de 30 dias do término do prazo vigente.
                                Não serão prorrogados projetos com calendário previamente informado ou historicamente definida, bem como planos anuais.
                            </p>
                            
                            <div class="dept-title-tabs">Por quanto tempo o projeto poderá ser prorrogado?</div>
                            <p>
                                O prazo máximo para captação será de até 24 meses a partir da data de publicação da portaria de autorização para captação, exceto em caso fortuito ou força maior, comprovado. Ressaltamos que:
                                <br>
                                a) O prazo pode ser ampliado para 4 exercícios fiscais para projetos de recuperação do patrimônio histórico ou construção de imóveis, conforme característica do projeto e complexidade da obra.
                                <br>
                                b) Não serão prorrogados projetos relativos a planos anuais de atividades e projetos com calendário específico com data previamente informada ou historicamente definida.
                            </p>
                            
                            <div class="dept-title-tabs">Qual o procedimento a ser feito quando o prazo para captação termina e eu não captei 100% do valor aprovado para o projeto?</div>
                            <p>
                                1) Caso haja o intuito de captar o saldo restante, é necessário solicitar a prorrogação do período de captação de recursos, até 30 dias corridos antes do fim do prazo estabelecido na portaria de aprovação do projeto, publicada no Diário Oficial da União. A prestação de contas poderá ser apresentada de forma parcial, por iniciativa do beneficiário ou quando solicitada pelo ministério.
                                <br>
                                2) Caso não haja interesse em captar o saldo restante:
                                <br>
                                a) O proponente deverá solicitar a prorrogação do prazo de execução, também até 30 dias corridos antes do prazo estabelecido em portaria de aprovação, caso o projeto possa ser executado conforme pactuado com o MinC;
                                <br>
                                b) Ou deverá ser apresentada a prestação de contas do montante captado em até 30 dias a contar da finalização do prazo de execução, finalizando o projeto. 
                            </p>
                            
                            <div class="dept-title-tabs">Caso eu não solicite a prorrogação de captação e o prazo do projeto expirar, o que acontece?</div>
                            <p>
                                1) Se houver captação e a execução do projeto tiver começado, o proponente deverá apresentar a prestação de contas;
                                <br>
                                2) Se houver captação:
                                <br>
                                2.1) e os recursos não tiverem sido utilizados, o projeto será arquivado, sendo que é responsabilidade do proponente solicitar o arquivamento do projeto e conceder anuência para o recolhimento dos recursos ao Fundo Nacional da Cultura;
                                <br>
                                2.2) e o projeto for Plano Anual, o proponente poderá transferir os recursos para o projeto em sequência, solicitando a transferência de recursos identificando os 2 projetos envolvidos;
                                <br>
                                3) Se não tiver havido captação, o projeto será arquivado.
                            </p>
                            
                            <div class="dept-title-tabs">O prazo de execução do projeto expirou enquanto estava em análise na unidade vinculada ao MinC. Como proceder acerca da vigência?</div>
                            <p>O proponente deve estar sempre atento ao prazo de vigência do projeto. Por questões técnicas, não é possível prorrogar os projetos que estejam com as unidades vinculadas ao MinC. Entretanto, caso o proponente tenha se manifestado sobre o prazo, assim que o processo for recebido pela Sefic terá o pedido de prorrogação analisado e, se for o caso, o projeto terá sua vigência regularizada.</p>
                            
                            <div class="dept-title-tabs">Qual o prazo para pedir reconsideração/recurso de uma decisão proferida pelo MinC?</div>
                            <p>Até dez dias corridos, a partir do seu registro no SALIC. Por isso, é muito importante o acompanhamento constante do sistema para verificar atualizações no sistema.</p>
                            
                            <div class="dept-title-tabs">O prazo para apresentação da prestação de contas final é considerado período de execução do projeto?</div>
                            <p>Não. O prazo para apresentação da Prestação de Contas Final é de 30 dias a contar do encerramento do prazo de execução do projeto, ou seja, o proponente não está apto a executar metas físicas ou financeiras nesse período.</p>
                            
                        </div>
                        
                        <div class="tab-pane" id="divSistema">
                            <div class="dept-title-tabs">Não é possível realizar o pedido de alteração (local de realização, plano de distribuição, nome, proponente) pelo Novo Salic. Como devo proceder?</div>
                            <p>O sistema permite o registro desse tipo de solicitação apenas nos casos que tenha havido a captação de, no mínimo, 20% do valor total aprovado ou 1/12 para Plano Anual de Atividades. Nas demais situações, as demandas devem ser encaminhadas ao MinC, via postal.</p>
                            
                            <div class="dept-title-tabs">Não é possível realizar o pedido de prorrogação de captação/execução pelo Novo Salic. O que devo fazer?</div>
                            <p>Após a finalização do prazo de captação/execução vigente não será possível realizar o pedido de prorrogação pelo sistema, uma vez que o módulo de solicitação é bloqueado para projetos com prazos expirados, cuja solicitação de prorrogação não foi efetuada com 30 dias de antecedência da data final. Desse modo, orientamos o proponente a encaminhar pedido ao MinC, em meio físico. A solicitação da prorrogação de prazo, realizada intempestivamente, será analisada caso a caso.</p>
                        </div>

