                    <div class="tab-content col-md-8 col-sm-8 col-xs-7 pull-right" style="text-align: justify;">

                        <div class="tab-pane active" id="divComprovacao">
                            <div class="dept-title-tabs">Como comprovar a execução das medidas de Acessibilidade?</div>
                            <p>O proponente deverá discorrer brevemente sobre todas as medidas adotadas para facilitar o acesso das pessoas com alguma limitação física (pessoas com problema de mobilidade, idosos, surdos, cegos, obesos e/ou outros).  Todas as informações devem ser atestadas por meio de documentos comprobatórios, tais como: registro fotográfico e/ou videográfico dos espaços utilizados para realização do evento, com foco nos instrumentos de acessibilidade, como elevador, rampas, banheiros adaptados, rotas de fuga acessível, assentos especiais (frontais para idosos e mais largos para obesos), comprovação da utilização de audiodescrição, estenotipia (closed caption), fonte ampliada, intérprete de libras, declarações emitidas pela direção dos espaços locados e/ou plantas arquitetônicas e envio de amostras dos materiais produzidos em braille.</p>
                            
                            <div class="dept-title-tabs">Como comprovar a execução do Plano de Distribuição quando há comercialização do produto?</div>
                            <p>O proponente deverá discorrer brevemente sobre todas as ações executadas: preços de comercialização do(s) produto(s) cultural (ais), distribuição para patrocinadores, divulgação e beneficiários. Todas as informações devem ser atestadas por meio de documentos comprobatórios, tais como: borderôs (obrigatório em caso de apresentações realizadas em espaços fechados com cobrança de ingressos); documento (divulgação em sitios de livrarias, declarações e/ou notas fiscais de consignação) que comprove que livro, CD, catálogo etc  estão sendo comercializados pelo valor pactuado, declarações de recebimento e/ou cartas de agradecimento emitidas por pessoas e/ou instituições beneficiárias da cota gratuita, divulgação e patrocinadores.</p>
                            
                            <div class="dept-title-tabs">Como comprovar a execução das medidas de Democratização ao acesso público?</div>
                            <p>No campo medidas de democratização ao acesso, discorrer sobre todas as medidas realizadas para promover o acesso e fruição de bens, produtos e serviços culturais. Exemplo: realização em local de fácil acesso, disponibilização de transporte, distribuição gratuita de produto, entrada franqueada, atividades paralelas. Todas as informações devem ser atestadas por meio de documentos comprobatórios, tais como: declarações emitidas por pessoas e/ou instituições beneficiárias; registros fotográficos e/ou videográficos do evento; amostras e/ou fotos das peças de divulgação; clipping com matérias de jornais, revistas, internet e/ou outros veículos de comunicação.  Quando se tratar de seminários, simpósios, oficinas, workshops, enviar lista de presença dos participantes, com identificação documental (CPF e/ou RG). </p>
                            
                            <div class="dept-title-tabs">Produzi materiais de divulgação com recurso diverso do projeto, é necessário encaminhá-los na Prestação de Contas?</div>
                            <p>Devem ser apresentadas amostras e/ou fotos de todas as peças de divulgação executadas, mesmo aquelas que foram produzidas com recursos de outras fontes. Estas, também, devem estar de acordo com a legislação que rege o correto uso das Marcas do PRONAC.</p>
                            
                            <div class="dept-title-tabs">Como comprovar a execução do Plano de Distribuição no caso de total gratuidade do produto?</div>
                            <p>O proponente deverá discorrer brevemente sobre todas as ações executadas: informar sobre a gratuidade e as providências tomadas para que o bem, produto ou serviço cultural chegasse ao público-alvo. Como comprovação das informações prestadas deverá apresentar:  amostras e/ou fotos das peças de divulgação, clipping com matérias de jornais, revistas, internet e/ou outros veículos de comunicação ou qualquer tipo de documentação por meio da qual seja possível aferir que o evento teve entrada fraqueada. No caso de produto com tiragem (ex: livros, CDs, catálogos etc) dervá ser enviada declaração de recebimento emitida pela entidade recebedora ou relatório de envio dos Correios contendo o destinatário, descrição do produto e quantidade.</p>
                            
                            <div class="dept-title-tabs">Como comprovar a execução dos itens previstos do Plano de Divulgação? </div>
                            <p>Todas as informações devem ser atestadas por meio de documentos comprobatórios. Enviar clipping com matérias de jornais, revistas, internet e/ou outros veículos de comunicação. Além disso, é obrigatório o envio de amostras e/ou fotos de todas as peças de divulgação produzidas. O envio da arte do material não é documento comprobatório de sua execução.</p>
                            
                            <div class="dept-title-tabs">Como comprovar a execução dos objetivos e metas do projeto?</div>
                            <p>Após discorrer brevemente  (no campo OBJETIVOS) sobre os objetivos e metas efetivamente alcançados, o proponente deverá apresentar documentos que atestem as informações prestadas, tais como: declarações, clipping com matérias de jornais, revistas, internet e/ou outros veículos de comunicação, registros fotográficos e/ou videográficos, listas de participantes ou qualquer outro tipo de documentação por meio da qual seja possível aferir o alcance dos objetivos.</p>
                            
                            <div class="dept-title-tabs">Como identificar os beneficiários e a população de baixa renda para recebimento da distribuição gratuita do produto cultural previsto como medida de democratização?</div>
                            <p>São elegíveis como população de baixa renda quaisquer entidades sem fins lucrativos que atendam pessoas socialmente desfavorecidas, incluindo bibliotecas e  instituições públicas de ensino.</p>
                        </div>

                        <div class="tab-pane" id="divPrazo">
                            <div class="dept-title-tabs">Qual é o prazo para apresentar a prestação de contas final do projeto?</div>
                            <p>Após o término do prazo de execução do projeto, o proponente tem  30 dias consecutivos para apresentar a prestação de contas final. </p>
                            
                            <div class="dept-title-tabs">Não prestei contas no prazo estabelecido e meu projeto está na situação E-23:  Inadimplente, quais medidas tomar?</div>
                            <p>Quando as contas e demais documentos comprobatórios do cumprimento do objeto do projeto não forem apresentados no prazo estipulado o MinC registrará a inadimplência do proponente. A situação será regularizada com o envio da Prestação de Contas.</p>
                            
                            <div class="dept-title-tabs">Após a entrega da Prestação de Contas Final fui diligênciado para apresentar documentações/informações complementares no prazo de 20 dias. Posso pedir prorrogação do prazo caso seja necessário?</div>
                            <p>Sim. O prazo para resposta à diligência poderá ser prorrogado uma única vez, por 20 dias, a pedido do proponente.</p>
                        </div>
                        
                        <div class="tab-pane" id="divContaFinal">
                            <div class="dept-title-tabs">Após a entrega da Prestação de Contas Final ao MinC, meu projeto cultural está encerrado? </div>
                            <p>Após a entrega da Prestação de Contas Final, o projeto não é mais considerado ativo, entretanto o proponente ainda possui responsabilidades sobre as informações prestadas até análise final, que decidirá sobre aprovação ou reprovação do projeto. O proponente poderá ser diligenciado nesse período, para apresentar documentações e/ou informações complementares, bem como deverá manter sob sua guarda os documentos comprobatórios de execução do projeto pelo prazo de dez anos.</p>
                        </div>
                        
                        <div class="tab-pane" id="divContaSalic">
                            <div class="dept-title-tabs">Que tipo de arquivos são possíveis inserir na prestação de contas realizada via sistema SALIC?</div>
                            <p>Atualmente as extensões permitidas para upload de documentos no sistema SALIC são: pdf e todas as extensões de arquivos de imagem (ex: jpeg, jpg etc). O tamanho máximo permitdo para upload é de 5MB por documento.</p>
                            
                            <div class="dept-title-tabs">Preenchi todos os formulários, fiz o upload de todos os arquivos e realizei as comprovações físicas/financeiras e de cumprimento do objeto na Prestação de Contas via sistema SALIC, porém meu projeto está na situação E-23 Inadimplente. O que devo fazer?</div>
                            <p>É necessário verificar se a prestação de contas foi enviada, pois após o devido preenchimento das comprovações o sistema inclui na barra lateral esquerda a opção de envio, que antes do preenchimento das comprovações não estava disponível.</p>
                        </div>
                        
                        <div class="tab-pane" id="divSaldo">
                            <div class="dept-title-tabs">Finalizei a execução do projeto e restou saldo na conta livre/movimento, como proceder?</div>
                            <p>Caso tenha restado saldo credor após a finalização da execução, o proponente deverá realizar o recolhimento dos valores disponíveis na conta livre/movimentação do projeto ao Fundo Nacional de Cultura (FNC), através de Guia de Recolhimento da União e encaminhar a GRU acompanhada do comprovante de recolhimento juntamente com a documentação relativa à prestação de contas final.</p>
                            
                            <div class="dept-title-tabs">Como emitir GRU para recolhimento ao FNC de valores captados e não utilizados projeto?</div>
                            <p>É necessário acessar o endereço eletrônico <a href="http://consulta.tesouro.fazenda.gov.br/gru_novosite/gru_simples.asp">http://consulta.tesouro.fazenda.gov.br/gru_novosite/gru_simples.asp</a> e após a inserção dos dados descritos abaixo gerar a GRU:
                                <br>
                                <span style="font-weight: bold;">Código de Recolhimento:</span> 28852-7
                                <br>
                                <span style="font-weight: bold;">UG / Gestão:</span> 340001 / 00001
                                <br>
                                <span style="font-weight: bold;">Número de Referência:</span> ( PRONAC Nº              )
                                <br>
                                <span style="font-weight: bold;">Contribuinte:</span> (CNPJ e nome da entidade proponente)
                            </p>
